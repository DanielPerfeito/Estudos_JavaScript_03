//ARROW FUNCTIONS

/*As arrow functions são uma sintaxe mais concisa e simples para criar funções em JavaScript. sua sintaxe normalmente segue da seguinte maneira: () => {}.
Aqui estão alguns exemplos simples de como usar arrow functions:*/

//1-Arrow function que retorna a soma de dois números:
const soma = (a,b) => a + b;

console.log(soma(4,2));

//2-Arrow function que retorna o dobro de um numero:

const dobro = (num) => num * 2;

console.log(dobro(7));



