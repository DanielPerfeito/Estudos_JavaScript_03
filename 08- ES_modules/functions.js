//Funções

// A palavra-chave 'export' é usada aqui para tornar as funções disponíveis
// para outros módulos que importam este arquivo.

export function somar(n1, n2) {
    return n1 + n2;
  }
  
export function subtrair(n1, n2) {
    return n1 - n2;
  }
  
export function multiplicar(n1, n2) {
    return n1 * n2;
  }
  
export function dividir(n1, n2) {
    return n1 / n2;
  }